/* ---- Language state ------------------------------------------------------ */
const LANGS = ['en', 'zh'];
function getLang() {
  const stored = (typeof localStorage !== 'undefined') && localStorage.getItem('tanko_lang');
  return LANGS.indexOf(stored) >= 0 ? stored : 'en';
}
function setLang(l) {
  if (LANGS.indexOf(l) < 0) return;
  try { localStorage.setItem('tanko_lang', l); } catch (e) {}
}
/* t(): UI string. L(obj): pick localized field from a {en, zh} object. */
function t(key) {
  const l = getLang();
  return (I18N[l] && I18N[l][key]) || (I18N.en[key]) || key;
}
function L(obj) {
  if (obj == null) return '';
  if (typeof obj === 'string') return obj;
  const l = getLang();
  return obj[l] || obj.en || '';
}
